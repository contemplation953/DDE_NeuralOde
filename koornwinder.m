function u=koornwinder(n,t)
%该函数生成n阶Koornwinder多项式在t处的值
%输入参数为非负整数n和向量t
%t中元素的取值范围为[-1,1]
%输出为向量Kn(t)
T=length(t);
if n==0
    for i=1:T
        y(i)=1;
    end
else
    for i=1:T
        tmp=(n^2+1)*legendre(n,t(i));
        y(i)=tmp(1);
        for j=0:n-1
            tmp=(2*j+1)*legendre(j,t(i));
            y(i)=y(i)-tmp(1);
        end
    end
end
u=y;
